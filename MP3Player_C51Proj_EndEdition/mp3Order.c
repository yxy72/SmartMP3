unsigned char* PlaySongN (unsigned char a){
	uchar command[] = {0x7e,0x05,0x41,0x00,0x01,0x45,0xef,0xff};
	command[4]=a;
	command[5]=0x05^0x41^0x00^a;
	return command;
}
